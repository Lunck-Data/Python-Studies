# Atribuição simples "="

saldo = 500

print(saldo)
# 500

# Atribuição com adição

saldo = 500
saldo += 200

print(saldo)
# 700

# Atribuição com subtração

saldo = 500
saldo -= 200

print(saldo)
# 300

# Atribuição com multiplicação

saldo = 500
saldo *= 2

print(saldo)
# 1000

# Atribuição com divisão

saldo = 500
saldo /= 2

print(saldo)
# 250.0

# Divisão inteira

saldo = 500
saldo //= 2

print(saldo)
# 250

# Atribuição com módulo

saldo = 500
saldo %= 480

print(saldo)
# 20

# Atribuição com exponenciação

saldo = 80
saldo **= 2

print(saldo)
# 6400
