# [].pop
# .pop tira o último elemento adicionado na lista. Nesse caso o primeiro elemento a ser retirado é o "csharp"
# Posso dar a posição em indice para exclusão também

linguagens = ["python", "js", "c", "java", "csharp"]

linguagens.pop() # csharp
linguagens.pop() # java
linguagens.pop() # c
linguagens.pop(0) # python

print(linguagens) # "js"